              <h4>Procedimiento y detención ilegal</h4>
            </div>
            <p>En octubre de 2012, en el marco de una investigación que incluyó intercepciones telefónicas autorizadas por el Juez de Garantía, la Brigada Antinarcóticos de la PDI sorprendió a nuestro representado al interior de su departamento con 163,03 gramos de marihuana prensada del tipo paraguayo destinada –supuestamente– a tráfico, más la suma de $73.000 –presumiblemente– producto de las ventas de la sustancia señalada. A juicio de la Fiscalía, los hechos descritos eran constitutivos del delito de tráfico ilícito de estupefacientes en pequeñas cantidades, acusando la participación de nuestro representado en calidad de autor.<p>
            
            <br><p>Defensores Norte tomó la defensa de este caso, alegando que las pruebas aportadas por la fiscalía no fueron suficientes, no estando acorde con el espíritu del proceso penal para inculpar al encartado. Se demostró situaciones al límite de lo ilícito ya que el actuar policial hubo infracciones a normas constitucionales, junto a la vez que un juez de la República fue objeto de una mentira. Para esta defensa, el ingreso al domicilio del acusado fue ilegal, al igual que su detención y la de su pareja que no fue acusada y, que además, es hija de un asistente policial.<p>
            
            <blockquote class="text-left-align">
              <p>"No se puede llegar a una decisión condenatoria cuando el estándar de la prueba exigida por el Estado no es de alta calidad y eficiente para acreditar la responsabilidad penal de un inculpado".</p>
              <span class="author-name"> <a href="#.">Carlo Silva</a></span> </blockquote>

              <br><p>En vista de lo expuesto, la Segunda Sala del Tribunal de Juicio Oral en lo Penal de La Serena no pudo llegar a una decisión condenatoria, porque el estándar de prueba exigida por el Estado para condenar penalmente no fue de alta calidad y eficiente para acreditar la responsabilidad penal del inculpado. Existió dudas más que razonables acerca de las circunstancias de perpetración del ilícito y que la prueba de cargo se encontró contradicha, así como también, de la existencia del delito. Por tanto, absolvió de la acusación a nuestro defendido como autor en un delito de tráfico ilícito de estupefacientes en pequeñas cantidades.<p>
            
