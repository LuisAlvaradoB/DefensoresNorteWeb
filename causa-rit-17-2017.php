              <h4>Procedimiento y detención ilegal</h4>
            </div>
            <p>El Ministerio Público, luego de recibir información anónima, acusó a dos personas de Ovalle –un panadero y una estudiante– por el delito de microtráfico. Estos habrían sido vigilados por personal de la PDI y luego que –supuestamente– el inculpado vendiera 2,9 gramos de cannabis sativa a un agente revelador se procedió con autorización judicial a registrar e incautar el domicilio del acusado. Allí se encontró cannabis sativa elaborada, cinco balanzas digitales y la suma de $ 107.000 en dinero en efectivo. En cuánto a la inculpada se presumió su participación al encontrarse su cédula de identidad en uno de los dormitorios del domicilio registrado.<br>

            <br>El Estudio Jurídico Defensores Norte llevó la defensa de la inculpada, una joven de 28 años estudiante de Educación Parvularia. Carlos Silva Muñoz, defensor penal privado sostuvo que no hubo antecedentes previos que relacionaran a su defendida con la sustancia encontrada al interior del domicilio allanado, ya que ella residia en un lugar distinto y que el día de los hechos estuvo circunstancialmente en ese domicilio debido a que es propiedad de sus padres. El único elemento que la vinculaba a las diligencia efectuadas era una cédula de identidad vencida. </p>
            <blockquote class="text-left-align">
              <p>"No se puede llegar a una decisión condenatoria cuando el estándar de la prueba exigida por el Estado no es de alta calidad y eficiente para acreditar la responsabilidad penal de un inculpado".</p>
              <span class="author-name"> <a href="#.">Carlo Silva</a></span> </blockquote>
            <p> A su vez la defensa sostuvo la ilegalidad del ingreso policial por el hecho indiciario de la orden no era correcto y cuestionó la existencia real del agente revelador quien no se presentó al testificar en el juicio. Argumentó sobre la antijuridicidad material del hecho, basada en las exigencias del artículo 43 de la ley 20.000 y en problemas de tipicidad del artículo 4° de la ley 20.000. Por tanto, ante lo expuesto el Tribunal Oral en lo Penal de Ovalle se vio inoficioso de razonar sobre la participación de la estudiante, absolviéndola del cargo imputados en su contra.</p>
