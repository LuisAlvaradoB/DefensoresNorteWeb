            <p>Hacer justicia es el valor que debe prevalecer en todo despacho de abogados. Bajo ésta premisa, nuestro estudio jurídico tuvo la satisfacción de ayudar a que dos jóvenes, incorrectamente acusados, lograran recuperar su libertad después de haber permanecido 210 días en prisión condenados por el supuesto delito de “robo con violencia” en un almacén local. La demandante –propietaria del negocio– sostuvo que ella y un familiar fueron asaltados y agredidos al interior del recinto, arrebatandoles la suma de $80.000.- y otras especies.<p>
            
            <br><p>Por la contraparte, ésta defensa no discutió la participación de los imputados, pero aclaró que éstos ingresaron al local de la víctima con la intención de hacer una “machimba” en las máquinas tragamonedas –un truco casero para engañar a la máquina de juego– por lo que no se estaba frente un robo con violencia, sino más bien a un intento de “estafa o un hurto”. En el análisis de los hechos, la parte demandante no pudo probar el monto en dinero sustraído, ni la violencia ejercida para la apropiación de cosa mueble determinada.<p>
            
            <blockquote class="text-left-align">
              <p>"Es importante que el acusado se contacte inmediatamente con un abogado para valorar los hechos que se le imputan. En muchas ocasiones, si no se determinan correctamente cuestiones de hecho, es factible que personas acusadas injustamente reciban sentencias incorrectas que los condene o envíe directamente a prisión".</p>
              <span class="author-name"> <a href="#.">Carlo Silva</a></span> </blockquote>

              <br><p>Se demostró que las pruebas de cargo eran insuficientes para considerar a nuestros defendidos responsables del delito por el cual fueron imputados. Luego de un fuerte debate sobre los elementos del tipo penal, el Tribunal Oral en lo Penal de La Serena decidió inclinarse por la tesis de ésta defensa, concluyendo que los hechos acusados por la Fiscalía sólo eran constitutivos de un hurto simple, cuyo reproche ya estaba cumplido producto de la desproporcionada y excesiva medida cautelar impuesta durante el proceso: 8 meses de presidio.<p>

              <br><p>En situaciones similares, es importante que el acusado contacte inmediatamente a un abogado para valorar los hechos que se le imputan. En muchas ocasiones, si no se determinan correctamente cuestiones de hecho, es factible que personas acusadas injustamente reciban sentencias incorrectas que los condene o envíe directamente a prisión. Finalmente, el fallo absolvió a ambos jóvenes como autores responsables de un delito de robo con violencia, dejándose sin efecto la medida cautelar acordada contra ellos.<p>
            
